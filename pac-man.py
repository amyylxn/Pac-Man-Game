def chooseMap():
    maps = [tiles1, tiles2]
    return random.choice(maps)
walls = chooseMap()

def square(x, y):
    """Draw square using path at (x, y)."""
    path.up()
    path.goto(x, y)
    path.down()
    path.begin_fill()

    for count in range(4):
        path.forward(20)
        path.left(90)

    path.end_fill()

def offset(point):
    x = (floor(point.x, 20) + 200) / 20
    y = (180 - floor(point.y, 20)) / 20
    index = int(x + y * 20)
    return index


def valid(point):
    index = offset(point)

    if walls[index] == 0:
        return False

    index = offset(point + 19)

    if walls[index] == 0:
        return False

    return point.x % 20 == 0 or point.y % 20 == 0





def world():
    bgcolor('black')
    path.color('blue')

    for index in range(len(walls)):
        tile = walls[index]

        if tile > 0:
            x = (index % 20) * 20 - 200
            y = 180 - (index // 20) * 20
            square(x, y)

            if tile == 1:
                path.up()
                path.goto(x + 10, y + 10)
                path.dot(2, 'white')



def move():
    writer.undo()
    writer.write(state['score'])

    clear()

    if valid(pacmanPos + pacmanDir):
        pacmanPos.move(pacmanDir)

    index = offset(pacmanPos)

    if walls[index] == 1:
        walls[index] = 2
        state['score'] += 1
        x = (index % 20) * 20 - 200
        y = 180 - (index // 20) * 20
        square(x, y)

    up()
    goto(pacmanPos.x + 10, pacmanPos.y + 10)
    dot(20, 'yellow')



    for point, course, color in ghosts:
        if valid(point + course):
            point.move(course)
        else:
            options = [
                vector(5, 0),
                vector(-5, 0),
                vector(0, 5),
                vector(0, -5),
            ]
            plan = choice(options)
            course.x = plan.x
            course.y = plan.y

        up()
        goto(point.x + 10, point.y + 10)
        dot(22, color)
        

    update()

    for point, course, color in ghosts:
        if abs(pacmanPos - point) < 20:
            writer.goto(-25, -15)
            writer.write("Game Over", font=("Comic Sans MS", 50, "normal"), align="center")
            return

    ontimer(move, 100)


def change(x, y):
    if valid(pacmanPos + vector(x, y)):
        pacmanDir.x = x
        pacmanDir.y = y

